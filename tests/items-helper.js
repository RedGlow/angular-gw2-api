function permute(input) {
	var usedChars = [],
		permArr = [];
	function doPermute(input) {
		var i, ch;
		for (i = 0; i < input.length; i++) {
			ch = input.splice(i, 1)[0];
			usedChars.push(ch);
			if (input.length == 0) {
				permArr.push(usedChars.slice());
			}
			doPermute(input);
			input.splice(i, 0, ch);
			usedChars.pop();
		}
		return permArr;
	};
	return doPermute(input);
}

function removeDuplicates(input) {
	var i = 0;
	var existing = [];
	while(i < input.length) {
		var itemId = input[i];
		if(existing.indexOf(itemId) == -1) {
			existing.push(itemId);
			i++;
		} else {
			input.splice(i, 1);
		}
	}
}

function combine(a, min) {
	var fn = function(n, src, got, all) {
		if (n == 0) {
			if (got.length > 0) {
				all[all.length] = got;
			}
			return;
		}
		for (var j = 0; j < src.length; j++) {
			fn(n - 1, src.slice(j + 1), got.concat([src[j]]), all);
		}
		return;
	}
	var all = [];
	for (var i = min; i < a.length; i++) {
		fn(i, a, [], all);
	}
	all.push(a);
	return all;
}

function basePrepare($httpBackend, collection, baseUrl, extraArguments) {
	function w(url, response, st) {
		if(!st) {st = 200;}
		$httpBackend
			.when('GET', url)
			.respond(st, response);
	}
	var allItemIds = [];
	var allResponses = [];
	for(var i = 1; i < extraArguments.length; i++) {
		var itemId = extraArguments[i];
		allItemIds.push(itemId);
		var itemResponse = collection[itemId];
		allResponses.push(itemResponse);
	}
	var singleInvalidResponse = {
		"text": "no such id"
	};
	var multipleInvalidResponse = {
		"text": "all ids provided are invalid"
	};
	allItemIds.push(999);
	var subsets = combine(allItemIds, 1);
	for(var k = 0; k < subsets.length; k++) {
		var itemIds = subsets[k];
		var itemIdsPermutations = permute(itemIds);
		for(var i = 0; i < itemIdsPermutations.length; i++) {
			var itemIdsPermutation = itemIdsPermutations[i];
			removeDuplicates(itemIdsPermutation);
			var itemResponses = [];
			for(var j = 0; j < itemIdsPermutation.length; j++) {
				var x = itemIdsPermutation[j];
				if(x == 999) continue;
				itemResponses.push(collection[x]);
			}
			var url = baseUrl + '?ids=' + itemIdsPermutation.join(',');
			var status = 200;
			if(itemResponses.length == 0) {
				itemResponses = itemIds.length == 1 ? singleInvalidResponse : multipleInvalidResponse;
				status = 404;
			}
			w(url, itemResponses, status);
			if(itemIdsPermutation.length == 1) {
				var url = baseUrl + '/' + itemIdsPermutation[0];
				w(url, itemResponses[0], status);
			}
		}
	}
	w(baseUrl + '?ids=1999', '', 500);
	return allResponses;
};

